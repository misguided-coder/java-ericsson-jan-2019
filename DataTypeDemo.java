class DataTypeDemo {

	public static void main(String[] args) {

		System.out.println("Data types explained!!!!!");

		//1 byte size = 8 bits in memory (RAM)
		byte age = 15;
		System.out.println(age);
		
		//2 byte size = 16 bits in memory (RAM)
		short pages = 120; //number of pages in book
		System.out.println(pages);

		//4 byte size = 32 bits in memory (RAM)
		int chapters = 620; //number of chapters in book
		System.out.println(chapters);

		//8 byte size = 64 bits in memory (RAM)
		long salary = 1298000000L; 
		System.out.println(salary);

		//4 byte size = 32 bits in memory (RAM)
		float price = 5210.00F; 
		System.out.println(price);

		//8 byte size = 64 bits in memory (RAM)
		double mrp = 905210.00; 
		System.out.println(mrp);

		//2 byte size = 16 bits in memory (RAM)
		char letter = 'T'; 
		System.out.println(letter);

		//1 bit in memory (RAM)
		boolean pass = true; 
		System.out.println(pass);

	}

}